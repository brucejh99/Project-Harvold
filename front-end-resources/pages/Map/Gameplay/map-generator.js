// player object for testing purposes

function Character(x, y, colour) {
	this.tileFrom	= [16, 16];
	this.tileTo		= [16, 16];
	this.timeMoved	= 0;
	this.width     	= 14;
    this.height     = 14;
	this.position	= [x * 16 + 1, y * 16 + 1];
	this.delayMove	= 100;
    this.colour     = colour;
}
