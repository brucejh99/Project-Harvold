function drawMap(mapArray){
    for(var i = 0; i < height * width; i++) {
        if(mapArray[i] === 1) {
            ctx.fillStyle = '#00000080';
            ctx.fillRect(i % width * square, Math.floor(i/width) * square, square, square);
        } else {
            ctx.fillStyle = '#ffffff80';
            ctx.fillRect(i % width * square, Math.floor(i/width) * square, square, square);
        }
    }
}
